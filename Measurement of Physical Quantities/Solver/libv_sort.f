C***********************************************************
      SUBROUTINE Sortv(Nv,Ndx,A,B,LV)
C    -----  Sort vector (for vector processor). -----
C
C                             09 Jun 1999  Matsufuru, H.
C***********************************************************
C
      INCLUDE 'lattsize.h'
C------------Variables--------------------------------------
      DIMENSION A(Nv,Ndx),B(Nv,Ndx),LV(Nv)
C------------Main-------------------------------------------
      DO 100 idx = 1,Ndx 
      DO 100 iv  = 1, Nv
C
      A( iv, idx ) = B( LV(iv), idx )
C
 100  CONTINUE
C
C-----------------------------------------------------------
      RETURN
      END
